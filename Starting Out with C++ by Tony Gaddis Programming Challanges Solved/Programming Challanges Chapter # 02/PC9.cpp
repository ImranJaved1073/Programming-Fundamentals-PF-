#include <iostream>
using namespace std;
int main()
{
    cout << endl;
    cout << "A char uses " << sizeof(char) << " byte." << endl;
    cout << "An int uses " << sizeof(int) << " bytes. " << endl;
    cout << "A float uses " << sizeof(float) << " bytes. " << endl;
    cout << "A double uses " << sizeof(double) << " bytes. " << endl;
    cout << endl;

    return 0;
}
